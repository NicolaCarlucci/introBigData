package generics;

public class ECGValue {

	public double time;
	
	public double volt;

	public ECGValue() {
		super();
	}

	public double getTime() {
		return time;
	}

	public void setTime(double time) {
		this.time = time;
	}

	public double getVolt() {
		return volt;
	}

	public void setVolt(double volt) {
		this.volt = volt;
	}
	
	
	
}
